    var que1=document.getElementById('que1');
    var Ans1=document.getElementById('Ans1');
    que1.addEventListener('click',function()
    {
        if (Ans1.style.display === 'block') 
        {
            Ans1.style.display = 'none';
        } 
        else 
        {
            Ans1.style.display = 'block';
        }
    });

